# دریافت ورودی‌ها
n = int(input())
m = int(input())

# متغیر برای نگه‌داری حاصل جمع نهایی
result = 0

# حلقه‌ی بیرونی برای i از -10 تا m
for i in range(-10, m + 1):#4
    # حلقه‌ی داخلی برای j از 1 تا n
    for j in range(1, n + 1):#3
        # محاسبه‌ی مقدار (i + j)^3 * j^2 و اضافه کردن به نتیجه
        result += (i + j)**3 * j**2

# چاپ نتیجه‌ی نهایی
print(result)